<?php
/**
 * ITEA PHP: Design patterns
 */

namespace App\DesignPatterns\Models;

class User
{
    private $email;

    public function __construct($email)
    {
        $this->email = $email;
    }

    public function getEmail()
    {
        return $this->email;
    }

    public function setEmail($email)
    {
        $this->email = $email;
    }
}
